class Dictionary:
	def __init__(self):
		self.dictionary = aho_corasick.ACAutomaton()
	
	def download_sudachi(self, mode="core"):
		match mode:
			case "core":
				url = "http://sudachi.s3-website-ap-northeast-1.amazonaws.com/sudachidict-raw/20250129/core_lex.zip"
				csv_path = "./data/core_lex.csv"
			case _:
				print("error")
				return
		
		if os.path.exists(csv_path):
			print(f"{csv_path}はすでに存在します。ダウンロードをスキップします。")
			return
		
		os.makedirs("./data", exist_ok=True)
		
		res = requests.get(url).content
		bytes_io = io.BytesIO(res)
		zip = zipfile.ZipFile(bytes_io)
		zip.extractall("./data/")
		print("辞書ファイルをダウンロードして解凍しました。")
	
	def load_dictionary(self, mode="core"):
		match mode:
			case "core":
				csv_path = "./data/core_lex.csv"
			case _:
				print("error")
				return
		
		if not os.path.exists(csv_path):
			print(f"{csv_path}が存在しません。辞書をダウンロードしてください。")
			return

		df = pd.read_csv(csv_path, encoding="utf-8", header=None)
		
		# 処理開始時間を記録
		start_time = time.time()
		total_items = len(df)
		print(f"辞書データの読み込みを開始します。合計 {total_items} 件のエントリを処理します。")
		
		for i in range(total_items):
			word = df.iloc[i][0]
			self.dictionary.add(word)
			
			if (i + 1) % 1000 == 0 or i + 1 == total_items:
				elapsed_time = time.time() - start_time
				progress_percent = (i + 1) / total_items * 100
				print(f"進捗: {i + 1}/{total_items} ({progress_percent:.1f}%) 経過時間: {elapsed_time:.1f}秒")
				
		total_time = time.time() - start_time
		print(f"辞書データの処理が完了しました。所要時間: {total_time:.1f}秒")
		
		self.dictionary.set_failure_path()
		print("辞書を読み込みました。")

